    status=$(curl -s -z "$(date)" -w %{http_code} https://e4ftl01.cr.usgs.gov//MODV6_Dal_H/MOTA/MCD19A2.006/2017.01.01/MCD19A2.A2017001.h08v05.006.2018114112131.hdf | tail -1)
